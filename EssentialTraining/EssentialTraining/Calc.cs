using System;

namespace EssentialTraining
{
  public class Calc
  {
    public int AddTwo(int a, int b)
    {
      return a + b;
    }

    public int SubTwo(int a, int b)
    {
      return a - b;
    }

    public int MultiplyTwo(int a, int b)
    {
      return a * b;
    }

    public int DivideTwo(int a, int b)
    {
      return a / b;
    }
  }
}
